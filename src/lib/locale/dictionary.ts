export type Locale = keyof typeof translations;
export type LocaleMessages = (typeof translations)[Locale];

export const defaultLocale: Locale = "en";
export const rtlLocales: Locale[] = ["ar", "ku"];

export function isLocale(value: unknown): value is Locale {
  return typeof value === "string" && value in translations;
}

export function translateFromDictionary(locale: Locale, key: string): string {
  const segments = key.split(".");
  let current: unknown = translations[locale] ?? translations[defaultLocale];

  for (const segment of segments) {
    if (
      current &&
      typeof current === "object" &&
      segment in (current as Record<string, unknown>)
    ) {
      current = (current as Record<string, unknown>)[segment];
    } else {
      return key;
    }
  }

  return typeof current === "string" ? current : key;
}
